package week1.task.javapgms;

public class BaseCalc {
	
	public int c;
	
	public int add(int a,int b)
	{
		c=a+b;
		return c;
	}
	
	public int sub(int a,int b)
	{
		c=a-b;
		return c;
	}
	
	public int mul(int a,int b)
	{
		c=a*b;
		return c;
	}
	
	public int div(int a,int b)
	{
		c=a/b;
		return c;
	}

}
